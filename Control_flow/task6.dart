// Task 6: Write a for loop that iterates from 1 to 25. Inside the loop, use an if statement 
// to print only the numbers that are multiples of 4.
// Student: Farxod Qarshiyev
// University: New Uzbekistan University

void main() {
  for (int i = 1; i <= 25; i++) {
    if (i % 4 == 0) {
      print(i);
    }
  }
}
